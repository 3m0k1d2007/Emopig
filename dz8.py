# Задание 1 (3)
import random

# 1. Создаём список из 15 чисел в диапазоне от 1 до 100
numbers = [random.randint(1, 100) for _ in range(15)]
print("Исходный список:", numbers)

# 2. Сортируем список от большего к меньшему методом сортировки вставками
iterations = 0  # счётчик итераций

for i in range(1, len(numbers)):
    key = numbers[i]
    j = i - 1
    while j >= 0 and numbers[j] < key:  # условие для сортировки по убыванию
        numbers[j + 1] = numbers[j]
        j -= 1
        iterations += 1
    numbers[j + 1] = key

# 3. Выводим отсортированный список
print("Отсортированный список (от большего к меньшему):", numbers)

# 4. Выводим количество итераций
print("Количество итераций алгоритма:", iterations)



