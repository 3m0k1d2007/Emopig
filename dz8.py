# Задание 3 (5)
def sort_with_fixed_element(arr, k):
    """
    Сортирует массив в порядке возрастания, оставляя элемент с индексом k на месте.
    :param arr: исходный массив
    :param k: индекс элемента, который должен остаться на месте
    :return: отсортированный массив с фиксированным элементом
    """
    # Создаём копию массива без элемента с индексом k
    temp_arr = arr[:k] + arr[k + 1:]

    # Сортируем копию
    temp_arr.sort()

    # Вставляем элемент с индексом k обратно на его позицию
    result = temp_arr[:k] + [arr[k]] + temp_arr[k:]

    return result


# Пример использования
array = [5, 2, 8, 1, 9, 3]
k = 2  # индекс элемента, который должен остаться на месте

print("Исходный массив:", array)
print("Индекс фиксированного элемента:", k)
print("Элемент с индексом", k, ":", array[k])

sorted_array = sort_with_fixed_element(array, k)
print("Отсортированный массив (элемент с индексом", k, "на месте):", sorted_array)